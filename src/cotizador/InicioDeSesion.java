    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            SaveUsers ob = new SaveUsers();
            for (int i = 0; i < ob.obtener().size(); i++) {
                if (ob.obtener().get(i).getNombre().equals(user.getText())) {
                    if (ob.obtener().get(i).getContraseña().equals(pass.getText())) {
                        AgregarComp x = new AgregarComp();
                        x.setVisible(true);
                        this.dispose();
                    }
                }
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error en login " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

